import datetime

class Reminder:

    int(__reminder_id)

    datetime(__time)

    #Returns the Unique ReminderID
    def get_reminder_id():
        return __reminder_id
    #Returns the Date and Time of the Reminder
    def get_time():
        return __time
    
    #Sets the Date and Time for the Reminder
    def set_time(self, date):
        self.date = date.date
        self.time = date.time


    

